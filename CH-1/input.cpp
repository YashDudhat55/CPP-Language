#include <iostream>
using namespace std;

int main()
{

    int a, b;

    cout << "Enter first number : ";
    cin >> a;
    cout << "Enter second number : ";
    cin >> b;

    cout << "A\t : " << a << endl;
    cout << "B\t : " << b << endl;

    cout << endl;

    cout << a << " + " << b << " = " << a + b << endl;

    return 0;
}